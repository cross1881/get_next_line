#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*d;
	char	*s;

	d = (char *)dst;
	s = (char *)src;
	if (d && s)
	{
		while (n)
		{
			d[n] = s[n];
			n--;
		}
		return (d);
	}
	return (NULL);
}
