#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;

	n = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
			n *= -1;
		str++;
	}
	while (ft_isdigit(*str))
		n = (*str++ - 48) + (n * 10);
	return (n);
}
