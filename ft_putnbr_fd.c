#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	while (n > 10)
	{
		n /= 10;
		n = (n % 10) + 48;
		ft_putchar_fd(n, fd);
	}
	n = (n % 10) + 48;
	ft_putchar_fd(n, fd);
}
